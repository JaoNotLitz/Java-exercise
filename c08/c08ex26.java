    public static void main(String[] args) {
        int b=0;
        while(b!=10){
        for(int a=0;a<=10;a++){
            System.out.println(" ".repeat(a)+"*");
        }
        for(int a=10;a>=0;a--){
            System.out.println(" ".repeat(a)+"*");
    }
    b++;
}
}
